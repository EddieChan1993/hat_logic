func (table *MainCharacterTalentTable) GetByTalentId(TalentId int32) (res []*MainCharacterTalentCfg) {
	for _, i := range table.mainCharacterTalentTalentIdIndexMap[TalentId] {
		res = append(res, table.Get(i))
	}
	return
}

func (table *MainCharacterTalentTable) GetByTalentSkill(TalentSkill int32) (res []*MainCharacterTalentCfg) {
	for _, i := range table.mainCharacterTalentTalentSkillIndexMap[TalentSkill] {
		res = append(res, table.Get(i))
	}
	return
}

func (table *MainCharacterTalentTable) GetByTalentIdAndLocation(TalentId int32, Location int32) (res []*MainCharacterTalentCfg) {
	for _, i := range table.mainCharacterTalentTalentIdAndLocationIndexMap[mainCharacterTalentTalentIdAndLocationIndex{TalentId, Location}] {
		res = append(res, table.Get(i))
	}
	return
}

type mainCharacterTalentTalentIdAndLocationIndex struct {
	talentId int32
	location int32
}
